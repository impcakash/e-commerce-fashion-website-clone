function displayProducts(array) {
    array.forEach(function (data) {
        let div = document.createElement("div");
        let finalDiv = document.createElement("div");

        let img1 = document.createElement("img");
        img1.src = data.display_img;

        let quickView = document.createElement("div");
        quickView.setAttribute("id", "quickView");
        quickView.textContent = "QUICKVIEW";


        let img = document.createElement("div");
        img.append(img1, quickView);

        let brand = document.createElement("div");
        brand.innerHTML = data.brand;
        brand.setAttribute("class", "w600")
        brand.style.color = "#b19975";

        let name = document.createElement("div");
        name.innerHTML = data.name;
        name.setAttribute("class", "loraf13");
        // name.setAttribute("class","lora","f13");

        let price = document.createElement("span");
        price.innerHTML = "Rs. " + data.price + "&nbsp;&nbsp;";
        price.setAttribute("class", "f14")

        let mrp = document.createElement("span");
        mrp.innerHTML = "Rs. " + data.MRP;
        mrp.style.textDecoration = "line-through";
        mrp.setAttribute("class", "f12")

        let discount = document.createElement("span");
        discount.innerHTML = "&nbsp;" + "(" + data.discount + "%" + " off)";
        discount.style.color = "#b19975";
        discount.setAttribute("class", "f12");

        let offer_price = document.createElement("span");
        // offer_price.innerHTML = "Offer Price: " + "Rs. " + data.offer_price;
        offer_price.innerHTML = "Offer Price: " + "&nbsp;&nbsp;";
        offer_price.setAttribute("class", "f12")

        let offervalue = document.createElement("span");
        offervalue.innerHTML = "Rs. " + data.offer_price;
        offervalue.style.color = "#3ab649";
        offervalue.setAttribute("class", "f12")

        let offer = document.createElement("div");
        offer.append(offer_price, offervalue);

        let value = document.createElement("div");

        value.append(price, mrp, discount);
        value.setAttribute("id", "value");

        div.append(brand, name, value, offer);
        div.style.textAlign = "center";
        div.style.lineHeight = "25px";

        finalDiv.append(img, div);
        div.onclick = () => {
            if (localStorage.getItem("bucket") == null) {
                localStorage.setItem("bucket", JSON.stringify([]));
            }
            // let arr = JSON.parse(localStorage.getItem("bucket"));
            // arr.push(data);
            // arr = new data;

            // console.log(arr);
            localStorage.setItem("bucket", JSON.stringify(data));

            window.location.href = "Productdetailspage.html";
        };

        parent.append(finalDiv);

        finalDiv.addEventListener("mouseenter", enter);

        finalDiv.addEventListener("mouseleave", leave);

        function enter() {
            quickView.style.display = "block";
        }
        function leave() {
            quickView.style.display = "none";
        }
        quickView.onclick = () => {
            if (localStorage.getItem("bucket") == null) {
                localStorage.setItem("bucket", JSON.stringify([]));
            }
            // let arr = JSON.parse(localStorage.getItem("bucket"));
            // arr.push(data);
            // arr = new data;

            // console.log(arr);
            localStorage.setItem("bucket", JSON.stringify(data));
            displayFromBucket();
            popupPoductDetails();
        };

        // console.log(parent);
    });
}

